package Basics;

public class Literals {
    public static void main(String[] args) {
        byte age=14;
        short age2=25;
        int age3=34;
        long age4=462765373375L;         //Long literal are Denoted by [L or l]
        float literal=34.55F;       //Float literal are Denoted by [F or f]
        double l2=34.5678;
        char l3='D';            // Char Literal are denoted by ['value'] Single inverted commas
        boolean r=true;
        String str="Naman";           //String literal are written in["value"] Double inverted commas
        System.out.println(literal);


    }
}
